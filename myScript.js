let viewMode = true; // true = pos, false = neg

//다크모드 적용 함수

function applyDarkModeStyle() {
    const logoInfoElements = document.querySelectorAll('.logoInfo');

    logoInfoElements.forEach(info => {
        if (viewMode) {  ///if 뷰모드가 true라면 검은바탕/흰글씨
            info.style.backgroundColor = 'rgba(255, 255, 255, 1)';
            info.style.color = 'black';
        } else {    ///if 뷰모드가 false라면 흰바탕/검은글씨
            info.style.backgroundColor = 'rgb(0, 0, 0, 0.9)';
            info.style.color = 'white';
        }
    });
}

function toggleDarkMode() {
    var body = document.body;

    if (viewMode) {
        body.classList.add('darkMode');
    } else {
        body.classList.remove('darkMode');
    }
    toggleImages();

    viewMode = !viewMode; ///뷰모드 바꿔주는 역할
}

function toggleImages() {
    var logoContainers = document.querySelectorAll('.logoContainer');

    ///foreach로 반복 logoContainers를 명명하는 container
    logoContainers.forEach(container => {
        var logoImage = container.querySelector('img');
        var logo = logos[container.id];

        if (logoImage && logo) {
            if (viewMode) {
                logoImage.src = 'img/' + logo.negImage
            }
            else {
                logoImage.src = 'img/' + logo.posImage
            }
        }
    });
}


function hideLogos() {
    for (var logoName in logos){
        var logoContainer = document.getElementById(logoName);
        if (logoContainer){
            logoContainer.style.display = 'none';
        }
    }
}

function showLogos(sortedLogos) {
    ////기존 있던거 제거
    var container = document.getElementById('logoBox');

    while (container.firstChild) {
        container.removeChild(container.firstChild);
    }


    ////로고컨테이너 설정
    sortedLogos.forEach(function (logoName) {
        var logo = logos[logoName];
        var logoContainer = document.createElement('div');
        logoContainer.className = 'logoContainer';
        logoContainer.id = logoName;

        ///logoimage 나오게하는거
        var logoImage = document.createElement('img');
        if (viewMode) {
            logoImage.src = 'img/' + logo.posImage;
        } else {
            logoImage.src = 'img/' + logo.negImage;
        }
        logoImage.style.transform = "scale(0.6)"

        ///logoinfo 만드는 함수
        var logoInfo = document.createElement('div');
        logoInfo.className = 'logoInfo';
        logoInfo.innerHTML = '<p>' + logo.name + '</p>';
        logoInfo.innerHTML += '<p>' + logo.region + '</p>';
        logoInfo.innerHTML += '<p>' + logo.object + '</p>';
        logoInfo.innerHTML += '<p>' + logo.year + '년 제작</p>';

        logoContainer.appendChild(logoImage);
        logoContainer.appendChild(logoInfo);
        container.appendChild(logoContainer);

        logoInfo.style.display = 'none';

        logoContainer.addEventListener('mouseover', function () {
            logoInfo.style.display = 'block';
            logoInfo.style.position = 'absolute';
            logoImage.style.transform = "scale(1.1)"
            logoImage.style.transition = "all 0.5s"
            logoImage.style.position = 'flex'

            applyDarkModeStyle();
        });

        logoContainer.addEventListener('mouseout', function () {
            logoInfo.style.display = 'none';
            logoImage.style.transform = "scale(0.6)"
            logoImage.style.transition = "all 0.5s"
        });
    });
}

function key(property, value) {
    hideLogos();

    if (property === 'region' || property === 'object') {
        sortedLogos = Object.keys(logos).sort(function(a, b) {
            return logos[a][property].localeCompare(logos[b][property]);
        }); 
    } 

    showLogos(sortedLogos.filter(function (logoName) {
        var logo = logos[logoName];
        return logo[property] === value || (property === 'key' && logo[property] === value); 
    }));
}


function card() {
    hideLogos();
    var sortedLogos = Object.keys(logos).sort(function(a, b) {
        return logos[a].name.localeCompare(logos[b].name);
    });

    showLogos(sortedLogos)
}

function year() {
    hideLogos();
    var sortedLogos = Object.keys(logos).sort(function(a, b) {
        return logos[a].year - logos[b].year;
    });

    showLogos(sortedLogos);
}




